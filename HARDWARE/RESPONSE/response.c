unsigned char CheackDat( unsigned char *dat)
{
	if((dat[0]==0x42)&&(dat[1]==0x4D))
	{
		LaserPm25 = 10 *( (dat[12]<<8)|(dat[13]) );
		LaserPm10 = 10 *( (dat[14]<<8)|(dat[15]) );
		PmErrorCnt = 0;
		return 1;
	}
	else
	{
		if( PmErrorCnt++ > ErrorMax ){
			LaserPm10 = LaserPm25 = 0;
			PmErrorCnt = ErrorMax;
		}
		return 0;
	}
}


