void Rest()
{    
	unsigned char len =0,t=0;
	CleanSent_Buffer();
	USART_TX_BUF[0] = START;
	USART_TX_BUF[1] = ACK;
	USART_TX_BUF[2] = 0;
	USART_TX_BUF[3] = STOP;
	len	= 4;				
	for(t=0;t<len;t++)
	{ 
		USART_SendData(USART1, USART_TX_BUF[t]);  
		while(USART_GetFlagStatus(USART1,USART_FLAG_TC)!=SET);
		//等待发送结束
	}
}



